                    <p style="text-align: justify; font-size: 20px; margin:0;">
                        En palabras más específicas lo que se pretende con este proyecto es <strong>desarrollar y diseñar un chatbot operacional</strong> cada vez más inteligente que sea capaz de entender y manejar consultas de seguimiento cada vez más complejas para dar soporte a los tickets que generan los clientes.
                        Cualquier compañía que siga manejando únicamente el correo electrónico o el teléfono corre el riesgo de quedarse obsoleta.
                        Muchas compañías apuestan por <strong>chatbots </strong> para usarlos con propósitos conversaciones o de información, pero es el momento de ir más allá y aprovecharse de las ventajas de la IA, construyendo un chatbot operacional basado en el lenguaje natural y aprendizaje automático, que sea capaz de entender y manejar consultas cada vez más complejas de nuestros clientes; proporcionándoles una respuesta inmediata y cómoda a su consulta. A su vez, que nos brinde datos detallados y procesables de momentos problemáticos de nuestros clientes, ofreciendo una mejor experiencia para los clientes que requieren información y/o seguimiento con sus tickets de soporte.

                        
